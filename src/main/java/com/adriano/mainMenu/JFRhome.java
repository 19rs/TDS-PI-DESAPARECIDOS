    private void jLabel5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel5MouseClicked

        if (JTFnome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Informe um nome", "Erro!", HEIGHT, erro);
        } else if (JTFcpf.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Informe um CPF", "Erro!", HEIGHT, erro);
        } else if (JTFdtNascimento.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Informe uma data", "Erro!", HEIGHT, erro);
        } else if (JTFidade.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Informe uma idade", "Erro!", HEIGHT, erro);
        } else if (JTFaltura.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Informe uma altura", "Erro!", HEIGHT, erro);
        } else if (JTFpeso.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Informe um peso", "Erro!", HEIGHT, erro);
        } else if (JTFresidente.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Informe uma cidade", "Erro!", HEIGHT, erro);
        } else {
            // cria um datasource para conexão com o banco
            DataSource dataSource = new DataSource();
            DAOpessoas pessoa = new DAOpessoas(dataSource);

            // instancia um objeto
            ModelPessoas f = new ModelPessoas();

            // pega os dados do formulário
            f.setNome(JTFnome.getText());
            f.setCpf((JTFcpf.getText()));
            f.setDataNascimento(JTFdtNascimento.getText());
            f.setIdade((JTFidade.getText()));
            f.setAtura((JTFaltura.getText()));
            f.setPeso((JTFpeso.getText()));
            f.setResidente(JTFresidente.getText());
            f.setCaracteristicas(JTFcaracteristicas.getText());

            // executa a inserção que está no MunicipioDAO
            pessoa.inserir(f);

            limpa();

        }


    }//GEN-LAST:event_jLabel5MouseClicked

