    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DataSource dataSource = new DataSource();
        DAOpessoas cidadeDao = new DAOpessoas(dataSource);

        ArrayList<ModelPessoas> registros = cidadeDao.ConsultaUm(JTFpesquisar.getText());

        if (registros != null && !registros.isEmpty()) {
            DefaultTableModel modelo = (DefaultTableModel) JTconsulta.getModel();
            JBlimpar.setEnabled(true);
            JBExcluir.setEnabled(true);

            // limpa a consulta anterior
            modelo.setNumRows(0);

            for (ModelPessoas m : registros) {
                modelo.addRow(new Object[]{
                    m.getNome(),
                    m.getCpf(),
                    m.getDataNascimento(),
                    m.getIdade(),
                    m.getAtura(),
                    m.getPeso(),
                    m.getResidente(),
                    m.getCaracteristicas(),});
            }
            JTconsulta.setModel(modelo);

            // cria variável para o JTable
            JTconsulta.getColumnModel().getColumn(0).setPreferredWidth(20);
  

//            JTconsulta.getColumnModel().getColumn(1).setPreferredWidth(10);
//            JTconsulta.getColumnModel().getColumn(2).setPreferredWidth(10);
//            JTconsulta.getColumnModel().getColumn(3).setPreferredWidth(5);
//            JTconsulta.getColumnModel().getColumn(4).setPreferredWidth(5);
//            JTconsulta.getColumnModel().getColumn(5).setPreferredWidth(5);
//            JTconsulta.getColumnModel().getColumn(6).setPreferredWidth(5);
//            JTconsulta.getColumnModel().getColumn(7).setPreferredWidth(20);

            // fecha DataSource
            dataSource.closeDataSource();

        } else {
            JOptionPane.showMessageDialog(this, "Sem resultados", "Erro!", HEIGHT, erro);

        }

    }//GEN-LAST:event_jButton1ActionPerformed

