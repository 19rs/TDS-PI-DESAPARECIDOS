    public boolean verificaSenha(String senha) {

        DataSource dataSource = new DataSource();
        DAOlogin loginsDao = new DAOlogin(dataSource);

        for (ModelLogin a : loginsDao.Consulta()) {
            if (a.getSenha().equals(senha)) {
                return true;

            }
        }
        return false;
    }


